import type { AuditAnswers, BusinessGrowthReport } from "@/lib/businessAuditTypes";

function actionPlanBadge(index: number, total: number): string {
  if (index === 0) return "Quick win";
  if (index <= Math.min(2, total - 2)) return "This month";
  return "This quarter";
}

function escapeHtml(s: string) {
  return s
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;");
}

function listHtml(items: string[]) {
  return items.map((item) => `<li>${escapeHtml(item)}</li>`).join("");
}

