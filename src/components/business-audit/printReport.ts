/** Opens print dialog in-page — avoids blank about:blank tabs from window.open + noopener. */
export function printGrowthReport(
  report: BusinessGrowthReport,
  answers: AuditAnswers
) {
  const html = buildPrintHtml(report, answers);

  const iframe = document.createElement("iframe");
  iframe.setAttribute("aria-hidden", "true");
  iframe.title = "Business Growth Report";
  Object.assign(iframe.style, {
    position: "fixed",
    right: "0",
    bottom: "0",
    width: "0",
    height: "0",
    border: "0",
    opacity: "0",
    pointerEvents: "none",
  });

  document.body.appendChild(iframe);

  const doc = iframe.contentDocument ?? iframe.contentWindow?.document;
  if (!doc) {
    iframe.remove();
    return;
  }

  doc.open();
  doc.write(html);
  doc.close();

  const win = iframe.contentWindow;
  if (!win) {
    iframe.remove();
    return;
  }

  const cleanup = () => {
    window.setTimeout(() => iframe.remove(), 1000);
  };

  const triggerPrint = () => {
    win.focus();
    win.print();
    cleanup();
  };

  if (doc.readyState === "complete") {
    window.setTimeout(triggerPrint, 100);
  } else {
    win.addEventListener("load", () => window.setTimeout(triggerPrint, 100), { once: true });
    window.setTimeout(triggerPrint, 500);
  }
}
